#include <iostream>
using namespace std;

void main()
{
	int A = 20;

	A += 10; // это A = A + 10; | this is A = A + 10;

	A -= 10; // это A = A - 10; | this is A = A - 10;

	A *= 10; // это A = A * 10; | this is A = A * 10;

	A /= 10; // это A = A / 10; | this is A = A - 10;

	cout << A << endl;
}

// Назвавние урока: Сокращенные арифметические формы +=, -= и т д.
